/**
 * Fleet Advisor — rule-table doc generator (Epic #3414 #3480, AC4).
 *
 * Auto-generates docs/generated/fleet-advisor-rules.md from config/fleet-advisor-rules.yml so the
 * human-readable rule catalogue can never drift from the source-of-truth data table. Run modes:
 *   node scripts/global/fleet-advisor-rules-doc.js            # write the doc
 *   node scripts/global/fleet-advisor-rules-doc.js --check    # exit 1 if the doc is stale (CI)
 */
'use strict';

const fs = require('fs');
const path = require('path');
const { loadRules } = require('./fleet-advisor-lint');

const DOC_PATH = path.join(__dirname, '..', '..', 'docs', 'generated', 'fleet-advisor-rules.md');

/** Render the rule table as a deterministic markdown document. */
function renderDoc(ruleset) {
  const lines = [];
  lines.push('<!-- GENERATED by scripts/global/fleet-advisor-rules-doc.js — do not edit by hand. -->');
  lines.push('<!-- Source of truth: config/fleet-advisor-rules.yml. Regenerate: npm run fleet-advisor:rules-doc. -->');
  lines.push('');
  lines.push('# Fleet Advisor — deterministic rule catalogue');
  lines.push('');
  lines.push(`Rule table version: **${ruleset.version}** · ${ruleset.rules.length} rules.`);
  lines.push('');
  lines.push('| Rule | Severity | Class | Tiers | Signal | Title |');
  lines.push('|---|---|---|---|---|---|');
  for (const r of ruleset.rules) {
    const tiers = Array.isArray(r.tiers) ? r.tiers.join(' ') : r.tiers || '*';
    lines.push(`| \`${r.id}\` | ${r.severity} | ${r.class} | ${tiers} | \`${r.signal}\` | ${r.title} |`);
  }
  lines.push('');
  lines.push('## Recommendations');
  lines.push('');
  for (const r of ruleset.rules) {
    lines.push(`- **${r.id}** — ${r.recommendation}`);
  }
  lines.push('');
  return lines.join('\n');
}

/** Build the doc text from the current rule table. */
function generate() {
  return renderDoc(loadRules());
}

function main() {
  const check = process.argv.includes('--check');
  const next = generate();
  if (check) {
    const current = fs.existsSync(DOC_PATH) ? fs.readFileSync(DOC_PATH, 'utf8') : '';
    if (current !== next) {
      process.stderr.write('fleet-advisor rule doc is stale — run: npm run fleet-advisor:rules-doc\n');
      process.exit(1);
    }
    process.stdout.write('fleet-advisor rule doc up to date\n');
    return;
  }
  fs.mkdirSync(path.dirname(DOC_PATH), { recursive: true });
  fs.writeFileSync(DOC_PATH, next);
  process.stdout.write(`wrote ${DOC_PATH}\n`);
}

if (require.main === module) main();

module.exports = { generate, renderDoc, DOC_PATH };
