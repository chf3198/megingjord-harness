'use strict';
// Refs #1295 — Migration backfill audit for legacy Epics #1130 #1103 #1113 #1211.
// Per Epic #1271 AC11. Runs reconciler on legacy Epics; emits per-Epic disposition
// (advisory comment OR EPIC_RESCOPE proposal OR follow-on ticket).

const { reconcileEpic, epicReadyToClose } = require('./epic-ac-reconcile');

const TARGET_EPICS = [1130, 1103, 1113, 1211];

function disposition(reconciled) {
  const measuring = reconciled.filter(r => r.truth_status === 'MEASURING');
  if (measuring.length && reconciled.every(r => r.truth_status === 'MEASURING' || r.truth_status === 'READY_TO_CLOSE' || r.rescope_ref)) {
    if (measuring.length === reconciled.length) {
      return { kind: 'measuring-window', reason: 'awaiting measurement; no immediate action', acs: measuring.map(m => m.ac_id) };
    }
  }
  if (epicReadyToClose(reconciled) && !measuring.length) return { kind: 'no-action', reason: 'all ACs satisfied or rescoped' };
  const unmet = reconciled.filter(r => r.truth_status === 'UNMET' && !r.rescope_ref);
  if (measuring.length && !unmet.length) {
    return { kind: 'measuring-window', reason: 'awaiting measurement; no immediate action', acs: measuring.map(m => m.ac_id) };
  }
  if (unmet.every(u => /4|5|7|9|10/.test(u.ac_id))) {
    return { kind: 'rescope-proposal', reason: 'unmet ACs deferable to follow-on', acs: unmet.map(u => u.ac_id) };
  }
  return { kind: 'advisory', reason: 'unmet ACs require Manager review', acs: unmet.map(u => u.ac_id) };
}

function buildAdvisoryComment(epicNum, reconciled, dispo) {
  return `## Backfill audit — Epic #${epicNum}\n\n` +
    `Disposition: **${dispo.kind}** — ${dispo.reason}\n\n` +
    (dispo.acs ? `Affected ACs: ${dispo.acs.join(', ')}\n\n` : '') +
    `### AC truth-status table\n\n` +
    `| AC | Checked | truth_status |\n|---|---|---|\n` +
    reconciled.map(r => `| ${r.ac_id} | ${r.checked} | ${r.truth_status} |`).join('\n') +
    `\n\n_Auto-generated by Epic #1271 AC11 backfill audit (#1295). Advisory only — Manager action required for rescope-proposal disposition._`;
}

async function auditEpic(github, owner, repo, epicNum) {
  const { data: issue } = await github.rest.issues.get({ owner, repo, issue_number: epicNum });
  const labels = (issue.labels || []).map(l => l.name || l);
  const hasMeasuring = labels.includes('status:measuring');
  const reconciled = reconcileEpic({ body: issue.body || '', hasMeasuringLabel: hasMeasuring });
  const dispo = disposition(reconciled);
  const comment = buildAdvisoryComment(epicNum, reconciled, dispo);
  return { epicNum, dispo, reconciled, comment };
}

async function run({ github, context, core, targets = TARGET_EPICS, dryRun = true }) {
  const { owner, repo } = context.repo;
  const reports = [];
  for (const epicNum of targets) {
    try {
      const r = await auditEpic(github, owner, repo, epicNum);
      reports.push(r);
      if (!dryRun && r.dispo.kind !== 'no-action') {
        await github.rest.issues.createComment({ owner, repo, issue_number: epicNum, body: r.comment });
      }
    } catch (e) {
      core.warning(`Audit failed for #${epicNum}: ${e.message}`);
    }
  }
  return reports;
}

module.exports = { auditEpic, disposition, buildAdvisoryComment, run, TARGET_EPICS };
